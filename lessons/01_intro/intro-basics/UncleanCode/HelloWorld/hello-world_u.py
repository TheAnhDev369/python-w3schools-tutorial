#   Sử dụng HÀM print() để in ra thông tin lên màn hình
print("Hello, World!");    # In ra dòng chữ "Hello, World!"

#   print() là một hàm trong Python dùng để hiển thị thông tin 
#  (thường là một chuỗi, số hoặc bất kỳ đối tượng nào) lên màn hình (hoặc output console).

#   Mọi thứ bên trong dấu ngoặc của print() sẽ được in ra. 
#   Ví dụ, print("Hello, World!") sẽ hiển thị "Hello, World!" trên màn hình.